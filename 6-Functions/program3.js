// Write a function that takes two numbers as arguments and returns their sum. Test the function by calling it with different values.

function sumNumbers(num1, num2) {
    return num1 + num2;
  }
  
  // Example usage of the function
  console.log(sumNumbers(5, 10)); // Output: 15
  console.log(sumNumbers(2, 7)); // Output: 9
  console.log(sumNumbers(-3, 8)); // Output: 5


