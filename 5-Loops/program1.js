// Using a while loop to print the numbers from 1 to 10 in JavaScript.

let i = 1;  // Initialize a variable i to 1
while (i <= 10) {  // Check if i is less than or equal to 10
  console.log(i);  // Print the value of i
  i++;  // Increment i by 1
}


// In this program, we first initialize a variable i to 1. Then, we use a while loop to repeatedly check if the value of i is less than or equal to 10. If it is, we print the value of i using console.log(), and then increment i by 1 using the ++ operator.

